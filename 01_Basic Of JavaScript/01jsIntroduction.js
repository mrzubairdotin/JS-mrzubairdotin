// --------- History of JavaScript ---------------
/* 
  JavaScript was created by Brendan Eich in 1995 while he was working at Netscape Communications Corporation. Originally, it was developed under the name "Mocha," but it was later renamed "LiveScript" before finally settling on "JavaScript.
  The initial purpose of JavaScript was to add interactivity to web pages. At that time, web pages were predominantly static, and there was a growing demand for more dynamic and interactive content. JavaScript provided a way to achieve this by allowing developers to manipulate the Document Object Model (DOM) of web pages, enabling actions like form validation, image slideshows, and more without relying solely on server-side processing.
*/

// --------- Javascript Versions -----------
/*
  JavaScript, invented by Brendan Eich, achieved the status of an ECMA standard in 1997 and adopted the official name ECMAScript. This language has evolved through several versions, namely ES1, ES2, ES3, ES5, and the transformative ES6. These updates have played a crucial role in improving and standardizing JavaScript, making it widely used and valuable in the ever-changing field of web development.
*/
