// ---------- What is JavaScript(JS) -----------
/* 
    JavaScript (JS) is a high-level, interpreted programming language primarily known for its use in web development. It was initially created to add interactivity to web pages in the early 1990s. Over time, JavaScript has evolved into a versatile language that can be used both on the client-side and server-side.
    JavaScript is a powerful and versatile language that is essential for modern web development, ranging from creating interactive web pages to building complex web applications and server-side services.
    HINDI : JavaScript (JS) एक उच्च स्तरीय, अनुप्रेषित प्रोग्रामिंग भाषा है जो मुख्य रूप से वेब विकास में उपयोग के लिए प्रसिद्ध है। यह प्रारंभिक रूप में 1990 के दशक में वेब पेजों में गतिशीलता जोड़ने के लिए बनाई गई थी। समय के साथ, JavaScript को सबसे प्रसिद्ध भाषा में बदला गया है जो क्लाइंट-साइड और सर्वर-साइड उपयोग के लिए उपयोग किया जा सकता है। JavaScript एक शक्तिशाली और बहुमुखी भाषा है जो आधुनिक वेब विकास के लिए अनिवार्य है, जिसमें इंटरैक्टिव वेब पेजों से लेकर जटिल वेब एप्लिकेशन और सर्वर-साइड सेवाओं तक का निर्माण शामिल है।
*/

